class Grader(X64Grader):
    @staticmethod
    def grade(answer: str) -> tuple[bool, list[tuple[str, str]]]:
        pre = """
            call bracket_match
            jmp end
        """

        post = """
            mov rax, 0
        end:
        """
        
        code = Grader.assemble(pre + answer + post)

        solved = True
        for _ in range(16):
            uc = Grader.setup_unicorn()

            if random.random() < 0.5:
                expected = 1
                rdi = random.choice(VALID)
            else:
                expected = 0
                rdi = random.choice(INVALID)

            uc.mem_write(0x20000, rdi.encode())

            uc.reg_write(UC_X86_REG_RDI, 0x20000)
            uc.reg_write(UC_X86_REG_RSI, 0x20100)

            Grader.run_unicorn(code, uc)

            if uc.reg_read(UC_X86_REG_RAX) != expected:
                solved = False
                break

        return solved, [
            ("Inputs", f"string: 0x20000\nscratch: 0x20100\n[string]: {rdi!r}"),
            ("Registers", Grader.register_snapshot(uc)),
            ("Memory", Grader.memory_snapshot(uc, 0x20000, 0x21100))
        ]
